// .indexOf()

// returns the first index at which a given element can be found in the array
// always returns -1 when it is NaN

// Time complexity: generally O(n) where n is the length of the array or string being searched

// example 1:
// const array = [2, 9, 9];
// array.indexOf(2); // 0
// array.indexOf(7); // -1
// array.indexOf(9, 2); // 2
// array.indexOf(2, -1); // -1
// array.indexOf(2, -3); // 0

// example 2:
// const message = [1,2,3,4,5]

// console.log(message.indexOf(4)) // returns 3

